 *
 */

#pragma once

#include "stdafx.h"

static const char* ${glsl}_${ext} =
EOS

    print $H $header;

    my $line;
    while(defined($line = <$GLSL>)) {
        chomp $line;
        $line =~ s/%/\\%/g;
        $line =~ s/"/\\"/g;
        print $H "\t\"$line\\n\"\n";
    }
    print $H "\t;\n";
}
